package bs.domain;

public class MediaType {
	private String name;
	private MediaLevel level;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public MediaLevel getLevel() {
		return level;
	}

	public void setLevel(MediaLevel level) {
		this.level = level;
	}
}
